"""
Модуль comp_interface.py

Предоставляет интерфейс ICompInterface для управления встроенным температурным компаратором
датчиков температуры (LM75, TMP75, TMP102, TMP117 и совместимые).

Интерфейс унифицирует работу с аппаратными компараторами различных производителей,
абстрагируя различия в терминологии и реализации регистров.

Основное назначение:
    - Настройка режима работы компаратора (термостатный режим или режим прерывания)
    - Установка температурных порогов T_min и T_max для детектирования выхода за диапазон
    - Контроль состояния вывода компаратора (ALERT/OS/INT)

Режимы работы компаратора:
    0 — Режим компаратора (Comparator/Therm mode).
        Вывод меняет уровень, пока температура находится за пределами диапазона [T_min, T_max].
        Возврат в исходное состояние происходит автоматически при нормализации температуры.
        Рекомендуется для аппаратных термостатов и систем охлаждения.

    1 — Режим прерывания (Interrupt/Alert mode).
        Вывод меняет уровень при выходе за пределы и удерживает его до чтения регистра состояния.
        Чтение регистра подтверждает событие и сбрасывает вывод в исходное состояние.
        Рекомендуется для генерации прерываний микроконтроллера.

Температурные пороги:
    - Единицы измерения: **градусы Цельсия (int)**.
      Пример: 25°C = 25, -10°C = -10.
    - Почему целые градусы: Обеспечивает совместимость со всеми LM75-подобными датчиками,
      включая базовые модели с разрешением 0.5°C (LM75).
    - T_min (нижний порог): срабатывание при температуре ниже этого значения
    - T_max (верхний порог): срабатывание при температуре выше этого значения
    - Формат: диапазон range(T_min, T_max) в градусах Цельсия (T_min < T_max обязательно)
    - Примечание: Для датчиков с высокой точностью значения округляются до целых градусов

Выход компаратора:
    - Тип: обычно открытый сток (Open-Drain), требуется подтягивающий резистор (4.7–10 кОм)
    - Уровень сигнала: настраиваемый (низкий или высокий при срабатывании)
    - Назначение: сигнализация о превышении температурных порогов

Этот интерфейс ориентирован на использование в проектах MicroPython.
Реализации должны обеспечивать надежное и понятное взаимодействие с аппаратным
компаратором и порогами температуры.

Предупреждение:
    Конкретная реализация зависит от датчика. Поведение сброса флагов, доступные
    диапазоны температур и разрешение могут отличаться. Смотрите документацию
    конкретного устройства!

Пример использования:
    from comp_interface import ICompInterface

    class MySensor(ICompInterface):
        def set_comp_mode(self, mode: int, level: bool = False) -> int:
            # Реализация настройки режима компаратора
            ...

        def set_thresholds(self, thresholds: tuple[float, float]) -> range:
            # Реализация установки температурных порогов (в градусах Цельсия!)
            ...

        def is_over_threshold(self) -> bool:
            # Реализация проверки состояния компаратора
            ...

    # Использование
    sensor = MySensor(i2c, address=0x48)

    # Настройка компаратора: режим прерывания, активный низкий уровень (0V при тревоге)
    sensor.set_comp_mode(mode=1, level=False)

    # Установка порогов: 20.4°C - 30.8°C
    sensor.set_thresholds((20.4, 30.8))

    Согласно общепринятой метрологической практике, погрешность средства измерений должна составлять не более 1/3 от ширины температурного окна (T_max – T_min).
    Это обеспечивает надёжное различение состояний «в пределах нормы» и «аварийное отклонение» и соответствует требованиям ГОСТ Р 8.736–2011 к соотношению систематической и случайной составляющих погрешности.

    Заметка:
        Существует правило "одной трети",
        которое гласит, что погрешность средства измерений (СИ) должна составлять не более 1/3 (а в ответственных случаях — до 1/5) от допуска (размера поля допуска) контролируемого параметра.
    Практический смысл правила "одной трети":
        Пусть допуск на температуру:
            T = 25 +/- 1 °C → поле допуска = 2 °C.
        Тогда по правилу "одной трети":
            Максимально допустимая погрешность СИ ≤ 2 °C / 3 ≈ 0.67 °C.

    Если использовать датчик с погрешностью +/-0.5 °C — это удовлетворяет требованию.
    Если использовать датчик с погрешностью +/-1 °C — это недопустимо, так как погрешность сравнима с самим допуском.

    # Проверка состояния
    if sensor.is_over_threshold():
        print("Температура превысила верхний порог!")

    # Чтение текущих порогов
    thresholds = sensor.set_thresholds(None)
    print(f"Пороги: {thresholds[0]}°C - {thresholds[1]} °C")

Примечание:
    - Все температуры в интерфейсе указаны в градусах Цельсия
    - Это обеспечивает совместимость со всеми LM75-подобными датчиками
    - В режиме прерывания вызов is_over_threshold() может сбросить флаг события
    - В режиме компаратора состояние сбрасывается автоматически при T < T_min
    - Параметр level: False = низкий уровень (0 Вольт), True = высокий уровень (V+) при срабатывании
"""

from micropython import const

class CompMode:
    """
     Режимы работы температурного компаратора.

     Example:
         sensor.set_comp_mode(mode=CompMode.COMPARATOR)
         sensor.set_comp_mode(mode=CompMode.INTERRUPT)

     Значения:
         COMPARATOR (0): Термостатный режим.
             Физический вывод ALERT активен, пока температура ВНЕ диапазона [T_min, T_max].
             Сбрасывается автоматически при возврате T в диапазон.

         INTERRUPT (1): Режим прерывания.
             Физический вывод ALERT активен при выходе T за пределы [T_min, T_max].
             Сбрасывается только чтением регистра состояния.
     """
    # Компаратор: Физический вывод ALERT активен, пока температура вне диапазона, где диапазон — это пороги,
    # установленные через ICompInterface.set_thresholds((T_min, T_max)):
    COMPARATOR = const(0)
    # Прерывание: физический вывод ALERT сбрасывается чтением в ~active_alarm_level, см. set_comp_mode
    INTERRUPT = const(1)


class ICompInterface:
    def set_comp_mode(self, mode: int | None = None, active_alarm_level: bool = False) -> int:
        """
        Установить режим работы встроенного температурного компаратора.

        Аргументы:
            mode (int | None): Режим работы компаратора.
                - CompMode.COMPARATOR (0) — термостатный режим.
                Вывод ALERT активен, пока температура ВНЕ диапазона [T_min, T_max],
                установленного через set_thresholds().
                Возврат в норму автоматически, когда T принадлежит диапазону [T_min, T_max].
                Рекомендуется для аппаратных термостатов.

                - CompMode.INTERRUPT (1) — режим прерывания.
                Вывод ALERT активен при выходе T за пределы диапазона [T_min, T_max].
                Сбрасывается только чтением регистра состояния.
                Рекомендуется для генерации прерываний микроконтроллера.

                - Если None — вернуть текущий режим без изменений.

        Внимание:
            - Выход компаратора — обычно открытый сток (Open-Drain)
            - Требуется внешний подтягивающий резистор (рекомендуется 4.7–10 кОм)
            - Для большинства применений используйте active_alarm_level=False
        """
        raise NotImplementedError()

    def set_thresholds(self, thresholds: tuple[float, float] | None = None) -> tuple[float, float]:
        """
        Устанавливает нижний и верхний пороги температуры (T_min, T_max).

        Аргументы:
            thresholds (T_min, T_max): пороги температуры в градусах Цельсия.
                start < stop обязательно!
            Если thresholds=None, возвращает текущие пороги без изменений.

        Возвращает:
            tuple[float, float]: Текущие пороги температуры (T_min, stop=T_max).

        Note:
            - При превышении T_max или падении ниже T_min вывод тревоги/ALERT меняет уровень
        """
        raise NotImplementedError()

    def is_over_threshold(self) -> bool:
        """
        Проверить, превышен ли верхний порог температуры (T > T_max).

        Возвращает:
            bool: True, если температура ВНЕ диапазона [T_min, T_max], False — иначе.

        Note:
            - В режиме INTERRUPT вызов может сбросить флаг (зависит от датчика).
            - В режиме COMPARATOR сброс происходит автоматически при T_min <= T <= T_max.
        """
