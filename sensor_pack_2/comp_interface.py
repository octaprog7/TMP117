"""
Модуль comp_interface.py

Предоставляет интерфейс ICompInterface для управления встроенным температурным компаратором
датчиков температуры (LM75, TMP75, TMP102, TMP117 и совместимые).

Интерфейс унифицирует работу с аппаратными компараторами различных производителей,
абстрагируя различия в терминологии и реализации регистров.

Основное назначение:
    - Настройка режима работы компаратора (термостатный режим или режим прерывания)
    - Установка температурных порогов Tmin и T_max для детектирования выхода за диапазон
    - Контроль состояния вывода компаратора (ALERT/OS/INT)

Режимы работы компаратора:
    0 — Режим компаратора (Comparator/Therm mode).
        Вывод меняет уровень, пока температура находится за пределами диапазона [Tmin, T_max].
        Возврат в исходное состояние происходит автоматически при нормализации температуры.
        Рекомендуется для аппаратных термостатов и систем охлаждения.

    1 — Режим прерывания (Interrupt/Alert mode).
        Вывод меняет уровень при выходе за пределы и удерживает его до чтения регистра состояния.
        Чтение регистра подтверждает событие и сбрасывает вывод в исходное состояние.
        Рекомендуется для генерации прерываний микроконтроллера.

Температурные пороги:
    - Единицы измерения: **градусы Цельсия (int)**.
      Пример: 25°C = 25, -10°C = -10.
    - Почему целые градусы: Обеспечивает совместимость со всеми LM75-подобными датчиками,
      включая базовые модели с разрешением 0.5°C (LM75).
    - Tmin (нижний порог): срабатывание при температуре ниже этого значения
    - T_max (верхний порог): срабатывание при температуре выше этого значения
    - Формат: диапазон range(Tmin, T_max) в градусах Цельсия (Tmin < T_max обязательно)
    - Примечание: Для датчиков с высокой точностью значения округляются до целых градусов

Выход компаратора:
    - Тип: обычно открытый сток (Open-Drain), требуется подтягивающий резистор (4.7–10 кОм)
    - Уровень сигнала: настраиваемый (низкий или высокий при срабатывании)
    - Назначение: сигнализация о превышении температурных порогов

Этот интерфейс ориентирован на использование в проектах MicroPython.
Реализации должны обеспечивать надежное и понятное взаимодействие с аппаратным
компаратором и порогами температуры.

Предупреждение:
    Конкретная реализация зависит от датчика. Поведение сброса флагов, доступные
    диапазоны температур и разрешение могут отличаться. Смотрите документацию
    конкретного устройства!

Пример использования:
    from comp_interface import ICompInterface

    class MySensor(ICompInterface):
        def set_comp_mode(self, mode: int, level: bool = False) -> int:
            # Реализация настройки режима компаратора
            ...

        def set_thresholds(self, rng: range) -> range:
            # Реализация установки температурных порогов (в градусах Цельсия!)
            ...

        def is_over_threshold(self) -> bool:
            # Реализация проверки состояния компаратора
            ...

    # Использование
    sensor = MySensor(i2c, address=0x48)

    # Настройка компаратора: режим прерывания, активный низкий уровень (0V при тревоге)
    sensor.set_comp_mode(mode=1, level=False)

    # Установка порогов: 20°C - 30°C (целые градусы)
    # range(20, 30)
    sensor.set_thresholds(range(20, 30))

    # Проверка состояния
    if sensor.is_over_threshold():
        print("Температура превысила верхний порог!")

    # Чтение текущих порогов
    current_range = sensor.set_thresholds(None)
    print(f"Пороги: {current_range.start}°C - {current_range.stop} °C")

Примечание:
    - Все температуры в интерфейсе указаны в целых градусах Цельсия (int)
    - Это обеспечивает совместимость со всеми LM75-подобными датчиками
    - Для датчиков с высокой точностью значения округляются до ближайшего целого градуса
    - В режиме прерывания вызов is_over_threshold() может сбросить флаг события
    - В режиме компаратора состояние сбрасывается автоматически при T < Tmin
    - Параметр level: False = низкий уровень (0V), True = высокий уровень (V+) при срабатывании
"""


